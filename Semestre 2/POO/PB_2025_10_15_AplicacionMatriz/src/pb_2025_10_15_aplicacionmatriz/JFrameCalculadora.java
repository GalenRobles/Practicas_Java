    public void mostrarMatrizC(){
          if(C==null)
              JOptionPane.showMessageDialog(rootPane, "Operacion no valida");
          else{
            jLabelRC.setText(C.getNumeroRenglones()+"");
            jLabelCC.setText(C.getNumeroColumnas()+"");
            
            modeloC.setRowCount(C.getNumeroRenglones());
            modeloC.setColumnCount(C.getNumeroColumnas());
            
            for(int r=0; r<C.getNumeroRenglones();r++){
             for(int c=0; c<C.getNumeroColumnas();c++){
                 modeloC.setValueAt(C.GetValor(r, c), r, c);
             }   
            }
          }
    }
    
    
    
