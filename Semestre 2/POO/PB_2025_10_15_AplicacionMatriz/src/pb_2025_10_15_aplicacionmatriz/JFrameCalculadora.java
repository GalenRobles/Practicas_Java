    private void jButtonCopiarReActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCopiarReActionPerformed
        // TODO add your handling code here:jCheck
        if(jRadioButtonMatrizA.isSelected()){
            jSpinRA.setValue(modeloC.getRowCount());
           jSpinCA.setValue(modeloC.getColumnCount());
           
           for(int r=0; r<modeloC.getRowCount();r++){
               for(int c=0; c<modeloC.getColumnCount();c++){
                   modeloA.setValueAt(modeloC.getValueAt(r, c), r, c);
               }
           }
        }
        else{
             if(jRadioButtonMatrizB.isSelected()){
            jSpinRB.setValue(modeloC.getRowCount());
           jSpinCB.setValue(modeloC.getColumnCount());
           
           for(int r=0; r<modeloC.getRowCount();r++){
               for(int c=0; c<modeloC.getColumnCount();c++){
                   modeloB.setValueAt(modeloC.getValueAt(r, c), r, c);
               }
           }
        }
        }
    }//GEN-LAST:event_jButtonCopiarReActionPerformed

